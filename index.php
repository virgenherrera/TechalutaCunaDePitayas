        <a name="Quienes_Somos"><h2>QUIENES SOMOS</h2></a>
        <p style="text-align:justify">Fundada el 22 de agosto del 2009, donde con apoyo del gobierno del estado de Jalisco de la secretaria de desarrollo rural aporto $1’500,000.00 como capital semilla, dinero destinado a 150 socios para préstamos a negocios productivos.

La cooperativa forma parte del SIFRA (Sistema De Financiamiento Rural y Alternativo).

La cooperativa de maneja con grupos llamados Gamep’s (grupos de ayuda mutua y esfuerzo propio) donde los socios se integran de esta manera para estar trabajando en conjunto.</p>

<div class="content-layout-wrapper layout-item-0">
<div class="content-layout layout-item-1">
    <div class="content-layout-row">
    <div class="layout-cell layout-item-2" style="width: 100%" >
		<a name="Valores"><h2>VALORES</h2></a>
        <p style="text-align:justify">Los directivos y empleados de nuestra cooperativa, basaran sus sistemas de operación y decisiones en la aplicación de los siguientes valores:</p>
        
        <ul style="text-align:justify">
          <li>Honestidad</li>
        </ul>
        <p style="text-align:justify">Es aquella cualidad humana por la que la <a href="http://www.monografias.com/trabajos7/perde/perde.shtml">persona</a> se  determina a elegir actuar siempre con base en la verdad y en la auténtica <a href="http://www.monografias.com/trabajos14/hanskelsen/hanskelsen.shtml">justicia</a> (dando a  cada quien lo que le corresponde, incluida ella misma).</p>
        <ul style="text-align:justify">
          <li>Respeto</li>
        </ul>
        <p style="text-align:justify">El respeto es aceptar y comprender tal y como son  los demás, aceptar y comprender su forma de pensar aunque no sea igual que la  nuestra</p>
        <ul style="text-align:justify">
          <li>Trabajo</li>
        </ul>
        <p style="text-align:justify">Acción que ofrece a cada hombre la oportunidad de  crecer, desarrollar todas sus capacidades congénitas, realizarse como persona.  Perfeccionando así su humanidad y manteniendo el predominio y superioridad efectiva  que debe ejercer sobre las cosas.</p>
        <ul style="text-align:justify">
          <li>Responsabilidad</li>
        </ul>
        <p style="text-align:justify">Administrar con eficacia el tiempo y los recursos  para obtener el máximo beneficio, capacidad humana para poder optar entre  diferentes opciones y actuar, haciendo uso de la libre voluntad, de la cual  resulta la necesidad que asumir todas aquellas consecuencias que de estos actos  se deriven</p>
        <ul style="text-align:justify">
          <li>Confianza</li>
        </ul>
        <p style="text-align:justify">Es la creencia en que una persona o grupo será  capaz y deseará actuar de manera adecuada en una determinada situación y  pensamientos. </p>
        <ul style="text-align:justify">
          <li>Ayuda Mutua</li>
        </ul>
        <p style="text-align:justify">Intercambio recíproco y voluntario de recursos,  habilidades y servicios por un beneficio mutuo entre ambas partes.</p>
        <ul style="text-align:justify">
          <li>Lealtad</li>
        </ul>
        <p style="text-align:justify">Es un corresponder, una  obligación que se tiene con los demás, compromiso de defender lo que creemos; y  en quienes creemos.</p>
        <p style="text-align:justify">&nbsp;</p>
        <ul style="text-align:justify">
          <li>Libertad</li>
        </ul>
        <p style="text-align:justify">Se refiere a la capacidad de actuar según nuestras  propias decisiones y escoger cada uno de los pasos que queremos dar y sus  diferentes aspectos</p>
        <ul style="text-align:justify">
          <li>Democracia</li>
        </ul>
        <p style="text-align:justify">Obliga a las personas al  respeto de las normas de convivencia social y desde<br>
          Luego a los ordenamientos  jurídicos, estimando el bien común, un bien más valioso que el bien de un sólo  individuo</p>
        <ul style="text-align:justify">
          <li>Igualdad</li>
        </ul>
        <p style="text-align:justify">El estado ideal de una sociedad, aquella en la cual  los derechos y obligaciones sean para todos iguales.</p>
        <ul style="text-align:justify">
          <li>Solidaridad</li>
        </ul>
        Colaboración mutua de la personas,  sentimiento que mantiene a las personas  unidas en todo momento. Abrirse  a la  posibilidad de recibir ayuda, y también estar dispuesto a darla. Implica la  búsqueda de una fraternidad que comienza entre dos personas, se extiende a la  sociedad, alcanza a un país entero, trasciende las fronteras y abarca a la  humanidad en su conjunto </div>

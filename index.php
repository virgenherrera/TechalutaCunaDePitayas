        <a name="Vision"><h2>Misión:</h2></a>
        <p style="text-align:justify">Mantener y superar los niveles Económicos, Administrativos y Financieros mediante la organización, planeación y capacitación de los socios tomando en cuenta la guía de los principales valores del cooperativismo universal importante y necesario para trabajar y convivir en beneficio común.</p>
    </div><div class="layout-cell layout-item-2" style="width: 50%" >
       <h2>Visión:</h2>
       <p style="text-align:justify">Conservar e incrementar nuestro liderazgo en la región, en el desarrollo integral de los principios básicos del Cooperativismo a través de la participación de los socios. Otorgando un servicio de calidad, desarrollando constantemente los recursos humanos siendo estos el principal capital de nuestra cooperativa.</p>

                                <h2 class="postheader">Socios</h2>
                                                
                <div class="postcontent postcontent-0 clearfix"><div class="content-layout-wrapper layout-item-0">
<div class="content-layout layout-item-1">
    <div class="content-layout-row">
    <div class="layout-cell layout-item-2" style="width: 100%" >
        <a name="Ingreso"><h2>INGRESO</h2></a>
        <p style="text-align:justify">El consejo de Administración hará saber mediante un comunicado público el periodo de ingreso para nuevos socios, Normalmente son en las reuniones de comisión permanente celebradas cada dos meses, o en la Asamblea Anual que se lleva a cabo en el mes de Febrero. Considerando los criterios siguientes en orden de importancia:</p>
        <ul style="text-align:justify">
          <li style="text-align:justify">Familiares directos de los socios (mayores de 18 años)</li>
          <li style="text-align:justify">Trabajadores de la cooperativa y sus familiares directos.</li>
          <li style="text-align:justify">Personas pertenecientes al municipio o residentes por mas de 10 años.</li>
          <li style="text-align:justify">Personas que presenten un proyecto factible de financiar.</li>
          <li style="text-align:justify">Personas Foráneas.</li>
        </ul>
    </div>
    </div>
</div>
</div>
<div class="content-layout-wrapper layout-item-0">
<div class="content-layout layout-item-1">
    <div class="content-layout-row">
    <div class="layout-cell layout-item-2" style="width: 100%" >
		<a name="Requisitos"><h2>REQUISITOS PARA SER SOCIO</h2></a>
		<ol>
		  <li style="text-align:justify">Llenar  una solicitud de ingreso dirigida al consejo de administración.</li>
		  <li style="text-align:justify">Compromiso  de cumplir con la ley, las bases constitutivas, el reglamento interior y las  demás disposiciones que se elaboren para el buen funcionamiento de la  cooperativa, esto es aceptar y cumplir con sus derechos y obligaciones que en  ellos se establecen.</li>
		  <li style="text-align:justify">Acta  de nacimiento</li>
		  <li style="text-align:justify">Comprobante  de domicilio </li>
		  <li style="text-align:justify">Curp</li>
		  <li style="text-align:justify">Credencial  de elector</li>
		  <li style="text-align:justify">Poseer  espíritu de trabajo, honradez, y estar en pleno goce de sus derechos civiles.</li>
		  <li style="text-align:justify">Pagar  un certificado de aportación (parte social 360 pesos)</li>
		  <li style="text-align:justify">Consumir  los servicios que la cooperativa preste, adquirir su seguro de vida si la  cooperativa ofrece ese servicio.</li>
		  <li style="text-align:justify">Ser aceptado y participar activa y responsablemente  en un GAMEP de la cooperativa.</li>
		  <li style="text-align:justify">Llenar una solicitud de financiamiento.</li>
		  <li style="text-align:justify">Aportar una   Garantía Original para el crédito</li>
		  </ol>
        
    </div>
    </div>
</div>

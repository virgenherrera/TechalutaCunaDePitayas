<div class="content-layout layout-item-1">
    <div class="content-layout-row">
    <div class="layout-cell layout-item-2" style="width: 30%" >
		<a name="Derechos"><h2>DERECHOS DE LOS SOCIOS</h2></a>
		<ol>
		  <li style="text-align:justify">Acudir  y participar en las asambleas generales de socios.</li>
		  <li style="text-align:justify">Tener  un solo voto, sea cual fuere el numero de certificados de aportación suscritos  y pagados.</li>
		  <li style="text-align:justify">Desempeñar  cargos y comisiones que le encomiende la asamblea general o los consejos  directivos. Un socio no podrá ocupar simultáneamente dos o más cargos de elección  en los consejos y comisiones.</li>
		  <li style="text-align:justify">Percibir  la parte que le corresponda de los rendimientos de cada ejercicio anual.</li>
		  <li style="text-align:justify">Solicitar  y obtener, mediante los conductos establecidos previamente, informes a los  consejos de administración y vigilancia, respecto de la marcha de la sociedad.</li>
		  </ol>
        
    </div><div class="layout-cell layout-item-2" style="width: 70%" >
		<a name="Obligaciones"><h2>OBLIGACIONES DE LOS SOCIOS</h2></a>
		<ol>
		  <li style="text-align:justify">Responder con el valor de los certificados o  aportaciones que suscriben de todas las aportaciones realizadas y obligaciones  contraídas por la sociedad, mientras forme parte de la cooperativa.</li>
		  <li style="text-align:justify">Desembolsar las aportaciones comprometidas: ya sea  como aportación social o cualquier otra cuenta que se solicite y que este  debidamente fundamentada y validada por la Asamblea general.</li>
		  <li style="text-align:justify">Asistir a las reuniones de  los consejos y comisiones: ya sea en las asambleas  ordinarias o extraordinarias u otras a las que convoque el consejo de  administración.</li>
		  <li style="text-align:justify">Cumplir con   los acuerdos debidamente pactados: la gestión democrática implica el  cumplimiento de los acuerdos que según la mayoría exigida legalmente se hayan adoptado  independientemente.</li>
		  <li style="text-align:justify">Participar en las actividades de la cooperativa en  la forma y cuantía establecida por los estatutos sociales y en los acuerdos de  la Asamblea General.</li>
		  <li style="text-align:justify">No realizar actividades de competencia con la  cooperativa, salvo autorización. La autorización la dará exclusivamente la  Asamblea General o el Consejo de Administración.</li>
		  <li style="text-align:justify">Participar en las actividades de formación,  capacitación y promoción cooperativa.</li>
		  <li style="text-align:justify">Guardar secreto sobre los asuntos y datos de la  cooperativa cuando su difusión pueda perjudicar a la misma. Si bien los socios  tienen derecho a conocer todo aquello que este relacionado con la gestión de la  cooperativa, al mismo tiempo, deberán asumir la responsabilidad de mantener en  secreto todos aquellos asuntos, que por su trascendencia, deban permanecer en  el ámbito interno de la cooperativa.</li>
		  <li style="text-align:justify">Mantener lazos de fraternidad con los demás socios y  procurar conservar la unidad y la armonía en la sociedad.</li>
		  <li style="text-align:justify">Observar una conducta solidaria respecto a las  necesidades y aspiraciones de los demás socios   y de responsabilidad personal en sus compromisos con la cooperativa.</li>
		  </ol>
